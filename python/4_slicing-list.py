number=[3,4,7,8,9,4,2,9,1,3,5,6]
print(number)

# slicing-list[start,end]
# in slicing,start is inclusive , end is exclusive

# slicing with +ve indexing start from 0 index,
# in -ve slicing indexing start like descending order
print(number[0:5])

print(number[2:7])

print (number[-5:-1])
print(number[-8:-5])